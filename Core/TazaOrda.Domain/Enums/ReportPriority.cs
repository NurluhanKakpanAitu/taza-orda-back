namespace TazaOrda.Domain.Enums;

/// <summary>
/// Приоритеты обращения/заявки
/// </summary>
public enum ReportPriority
{
    /// <summary>
    /// Низкий приоритет - некритичная проблема, может быть решена в плановом порядке
    /// </summary>
    Low = 0,

    /// <summary>
    /// Средний приоритет - требует внимания, но не критично
    /// </summary>
    Medium = 1,

    /// <summary>
    /// Высокий приоритет - требует срочного решения (свалки, серьёзные загрязнения)
    /// </summary>
    High = 2,

    /// <summary>
    /// Критический приоритет - чрезвычайная ситуация, требует немедленного реагирования
    /// </summary>
    Critical = 3
}